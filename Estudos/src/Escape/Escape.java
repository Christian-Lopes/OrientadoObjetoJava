package Escape;

public class Escape {

	public void espaco() {
		System.out.println("Teste de escape espaço\b !");
	}
	
	public void pularLinha() {
		System.out.print("Pular linha se o println!\n");
	}
	
	public void retorno() {
		System.out.println("Teste do escape retorno \r, tetse.");
	}
	public void tabulacao() {
		System.out.println("Nome \t Christia \t Idade \t 30.");
	}
	
	public void aspaSimples() {
		System.out.println("Teste \'da aspas simple\'.");
	}
	public void aspasDuplas() {
		System.out.println("Teste \"das aspas duplas\".");
	}
	public void controBarra() {
		System.out.println("Teste da barra invertida \\, tetse.");
	}
}
